package calculatorTDD;

public class Calculator {

    public Double add(Double a, Double b) {
        return a + b;
    }

    public Double subtract(Double a, Double b) {
        return a - b;
    }

    public Double pow(Double a, Double x) {
        return Math.pow(a, x);
    }

    public Double sqrt(Double a) {
        return Math.sqrt(a);
    }
}
